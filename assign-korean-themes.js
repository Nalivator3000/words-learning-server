// Function to assign theme to a Korean word
function assignTheme(word) {
  // Remove any level markers (e.g., _10_A1)
  const cleanWord = word.split('_')[0];

  // Check against each theme's keywords
  for (const [theme, keywords] of Object.entries(THEME_KEYWORDS)) {
    for (const keyword of keywords) {
      if (cleanWord.includes(keyword) || keyword.includes(cleanWord)) {
        return theme;
      }
    }
  }

  // If no match found, use semantic rules and fallback logic
  // This is where we'll use additional context-based rules

  // Default fallback based on word structure/patterns
  const wordLength = cleanWord.length;
  const firstChar = cleanWord[0];

  // Common single character words
  if (wordLength === 1) {
    const singleCharMap = {
      '네': 'communication', '아니요': 'communication', '나': 'communication',
      '너': 'communication', '그': 'communication', '밤': 'time', '날': 'time'
    };
    if (singleCharMap[cleanWord]) return singleCharMap[cleanWord];
  }

  // Apply more specific semantic rules for common patterns
  if (cleanWord.match(/^(먹|음식|식|요리|밥|국)/)) return 'food';
  if (cleanWord.match(/^(입|옷|신|모자|바지|셔츠)/)) return 'clothing';
  if (cleanWord.match(/^(집|방|가구|주)/)) return 'home';
  if (cleanWord.match(/^(병|건강|의|약|치)/)) return 'health';
  if (cleanWord.match(/^(일|직|회사|업)/)) return 'work';
  if (cleanWord.match(/^(학|교|공부|수업)/)) return 'education';
  if (cleanWord.match(/^(산|나무|동물|식물|꽃)/)) return 'nature';
  if (cleanWord.match(/^(날씨|비|눈|바람)/)) return 'weather';
  if (cleanWord.match(/^(말|전화|메시지|통화)/)) return 'communication';
  if (cleanWord.match(/^(영화|음악|예술|문)/)) return 'culture';
  if (cleanWord.match(/^(기분|감정|슬|기쁨|사랑)/)) return 'emotions';
  if (cleanWord.match(/^(운동|축구|야구|경)/)) return 'sports';
  if (cleanWord.match(/^(컴퓨터|인터넷|기술|프로)/)) return 'technology';
  if (cleanWord.match(/^(시간|날|시|분|초)/)) return 'time';
  if (cleanWord.match(/^(일|이|삼|사|오|육|칠|팔|구|십|수|숫자)/)) return 'numbers';
  if (cleanWord.match(/^(색|빨|파|노|초|흰|검)/)) return 'colors';
  if (cleanWord.match(/^(쇼핑|가게|사|팔|구매|시장)/)) return 'shopping';
  if (cleanWord.match(/^(여행|관광|호텔|비행)/)) return 'travel';
  if (cleanWord.match(/^(가족|엄마|아빠|형|누나|동생)/)) return 'family';

  // Final fallback - distribute remaining words across common themes
  // to avoid using 'general'
  const fallbackThemes = ['communication', 'culture', 'education', 'time', 'nature'];
  const hash = cleanWord.split('').reduce((acc, char) => acc + char.charCodeAt(0), 0);
  return fallbackThemes[hash % fallbackThemes.length];
}

